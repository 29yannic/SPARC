def power(base, exponent):
    """
    Calculates the power of a given base raised to an exponent.

    Parameters:
    base (float or int): The base number.
    exponent (float or int): The exponent to which the base is raised.

    Returns:
    float or int: The result of base ** exponent.
    """
    return base ** exponent

# Example usage
result = power(2, 3)  # 2^3 = 8
print(result)  # Output: 8